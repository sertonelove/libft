#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	k;
	size_t	i;

	k = 0;
	i = ft_strlen(s1);
	while (s2[k])
	{
		s1[i] = s2[k];
		i++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
